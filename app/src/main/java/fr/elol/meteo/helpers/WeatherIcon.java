    public static String getPicto (int picto, Boolean day) {
        if (day) {
            return pictoSun[picto];
        } else {
            return pictoMoon[picto];

        }
    }

    public static String getDirVent (Integer dir) {
        Integer mDir = (dir+22) / 45;
        return pictoDir[mDir];
    }

    public static String getBeaufort (Integer ventMoy) {
        if (ventMoy == 0)
            return WI_BEAFORT_0;
        else if (ventMoy <= 5)
            return WI_BEAFORT_1;
        else if (ventMoy <= 11)
            return WI_BEAFORT_2;
        else if (ventMoy <= 19)
            return WI_BEAFORT_3;
        else if (ventMoy <= 28)
            return WI_BEAFORT_4;
        else if (ventMoy <= 38)
            return WI_BEAFORT_5;
        else if (ventMoy <= 49)
            return WI_BEAFORT_6;
        else if (ventMoy <= 61)
            return WI_BEAFORT_7;
        else if (ventMoy <= 74)
            return WI_BEAFORT_8;
        else if (ventMoy <= 88)
            return WI_BEAFORT_9;
        else if (ventMoy <= 102)
            return WI_BEAFORT_10;
        else if (ventMoy <= 117)
            return WI_BEAFORT_11;
        else
            return WI_BEAFORT_11;
    }

    public static String getMoonphase (Integer mp) {
        return pictoMoonphase[mp % 28];
    }

    public static String getMoonphaseQuarters (Integer mp) {
        if (mp == null)
            return "";
        else
            return pictoMoonphaseQuarters[(mp/7)%4];
    }
